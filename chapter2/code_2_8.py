import pandas as pd

apt_df = pd.DataFrame({
    '자치구':    ['강남구', '서초구', '송파구', '강동구', '마포구'],
    '평균거래금액': [125000, 118000, 95000, 65000, 82000],
    '전용면적':   [84.5, 79.3, 76.2, 72.1, 68.4],
    '거래건수':   [245, 198, 312, 187, 156],
})

# 단일 조건 — 거래금액 10만 이상
high = apt_df[apt_df['평균거래금액'] >= 100000]
print('거래금액 10만 이상:\n', high)
print()

# 복합 조건 — AND (&)
result = apt_df[(apt_df['평균거래금액'] >= 80000) & (apt_df['거래건수'] >= 200)]
print('금액≥8만 AND 건수≥200:\n', result)
print()

# isin() 사용
targets = ['강남구', '마포구']
print(apt_df[apt_df['자치구'].isin(targets)])
