import pandas as pd

apt_df = pd.DataFrame({
    '자치구':    ['강남구', '서초구', '송파구', '강동구', '마포구'],
    '평균거래금액': [125000, 118000, 95000, 65000, 82000],
    '전용면적':   [84.5, 79.3, 76.2, 72.1, 68.4],
    '거래건수':   [245, 198, 312, 187, 156],
})

# 단일 열 선택 → Series
print(apt_df['자치구'])
print()

# 여러 열 선택 → DataFrame
print(apt_df[['자치구', '평균거래금액']])
print()

# 새 열 추가
apt_df['평당가격'] = (apt_df['평균거래금액'] / apt_df['전용면적']).round(0)
print(apt_df[['자치구', '평균거래금액', '평당가격']])
