import pandas as pd

apt_df = pd.DataFrame({
    '자치구':    ['강남구', '서초구', '송파구', '강동구', '마포구'],
    '평균거래금액': [125000, 118000, 95000, 65000, 82000],
    '전용면적':   [84.5, 79.3, 76.2, 72.1, 68.4],
    '거래건수':   [245, 198, 312, 187, 156],
})

print('평균:', apt_df['평균거래금액'].mean())
print('중앙값:', apt_df['평균거래금액'].median())
print('표준편차:', apt_df['평균거래금액'].std().round(2))
print('최소:', apt_df['평균거래금액'].min())
print('최대:', apt_df['평균거래금액'].max())
print()
print('=== 전체 요약 통계 ===')
print(apt_df.describe().round(1))
print()
print('=== 결측치 확인 ===')
print(apt_df.isnull().sum())
