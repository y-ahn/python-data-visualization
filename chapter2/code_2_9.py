import pandas as pd

apt_df = pd.DataFrame({
    '자치구':    ['강남구', '서초구', '송파구', '강동구', '마포구'],
    '평균거래금액': [125000, 118000, 95000, 65000, 82000],
    '전용면적':   [84.5, 79.3, 76.2, 72.1, 68.4],
    '거래건수':   [245, 198, 312, 187, 156],
})

# loc : 라벨(인덱스명, 컬럼명) 기반
print('=== loc ===')
print(apt_df.loc[0, '자치구'])                      # 단일 값
print(apt_df.loc[0:2, ['자치구', '평균거래금액']])   # 범위 + 컬럼
print()

# iloc : 정수 위치 기반
print('=== iloc ===')
print(apt_df.iloc[0, 0])      # 0행 0열
print(apt_df.iloc[0:3, 0:2])  # 0~2행, 0~1열
print()

# 조건 + loc 조합
print('=== 조건 + loc ===')
mask = apt_df['평균거래금액'] > 90000
print(apt_df.loc[mask, ['자치구', '평균거래금액']])
